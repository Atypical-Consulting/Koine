// Generates tooling/koine-ui/dist/_ds_adapter_entry.mjs — the bundle entry the design-sync
// converter reads (cfg.entry). @atypical/koine-ui ships PREACT components, but claude.ai/design's
// runtime renders with REACT 18, so each component is wrapped in a thin Preact->React MOUNT ADAPTER:
// window.KoineUi.<Name> becomes a real React component that mounts the real Preact subtree via
// preact's own render(). Verbatim reuse of the adapter proven in the sibling koine-studio sync
// (tooling/koine-studio/.ds-adapter/adapter.js). Run by cfg.buildCmd after each dist build; dist/ is
// gitignored, so this file is ephemeral and regenerated every build.
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const distDir = resolve(repoRoot, 'tooling/koine-ui/dist');
const dts = readFileSync(resolve(distDir, 'index.d.ts'), 'utf8');

// Components = PascalCase VALUE exports re-exported from ./components/* (skip `type X` and the
// SCREAMING_CASE constants like ASSISTANT_MOUNT_CLASS).
const names = new Set();
for (const m of dts.matchAll(/export\s*\{([^}]*)\}\s*from\s*'\.\/components\//g)) {
  for (const part of m[1].split(',')) {
    const id = part.trim();
    if (/^[A-Z][A-Za-z0-9]*$/.test(id)) names.add(id);
  }
}
const list = [...names];
if (!list.length) throw new Error('gen-adapter-entry: no PascalCase component exports found in dist/index.d.ts');

const entry = `// GENERATED by .design-sync/gen-adapter-entry.mjs — DO NOT EDIT. Preact->React mount adapter entry.
import { h as preactH, Fragment as preactFragment, render as preactRender } from 'preact';
import { ${list.map((n) => `${n} as _${n}`).join(', ')} } from './index.js';

// Re-exported so OWNED previews (.design-sync/previews/*.tsx) can build Preact-vnode fixtures — e.g. the
// deck surfaces' \`icon: (props) => JSX.Element\` — with the SAME bundled Preact instance the components
// use. Not part of @atypical/koine-ui's real API; preview-only plumbing.
export const h = preactH;
export const Fragment = preactFragment;

const React = window.React;
const ReactDOM = window.ReactDOM;

// Wrap a Preact component in a React component: the wrapper is a real React function component
// (using the runtime's window.React); inside a layout effect it renders the Preact tree into a
// container the React wrapper owns. React \`children\` are bridged both directions — React renders
// them into a detached host that is spliced into the Preact tree — so slots compose across the
// boundary. (Verbatim from koine-studio/.ds-adapter/adapter.js.)
function adapt(PreactComponent, displayName) {
  function Adapted(props) {
    const containerRef = React.useRef(null);
    const childBridge = React.useRef(null);
    React.useLayoutEffect(() => {
      const container = containerRef.current;
      if (!container) return;
      const { children, ...rest } = props;
      let preactChildren;
      if (children != null && children !== false) {
        if (!childBridge.current) {
          const host = document.createElement('div');
          host.style.display = 'contents';
          childBridge.current = { host, root: ReactDOM.createRoot(host) };
        }
        childBridge.current.root.render(children);
        const host = childBridge.current.host;
        preactChildren = preactH('div', {
          style: 'display:contents',
          ref: (el) => { if (el && host.parentNode !== el) el.appendChild(host); },
        });
      }
      preactRender(preactH(PreactComponent, rest, preactChildren), container);
    });
    React.useEffect(() => () => {
      if (containerRef.current) preactRender(null, containerRef.current);
      if (childBridge.current) childBridge.current.root.unmount();
    }, []);
    return React.createElement('div', { ref: containerRef, style: { display: 'contents' } });
  }
  Adapted.displayName = displayName;
  return Adapted;
}

${list.map((n) => `export const ${n} = adapt(_${n}, '${n}');`).join('\n')}

// ThemeSurface — the themed host these components require. @atypical/koine-ui defines its design
// tokens on :root (dark by default: --koi-paper, --koi-fg, --koi-font-body) but ships NO rule that
// paints the page — the consuming app establishes the surface. This is that surface, used as
// cfg.provider so every preview card renders on the real dark ground (and the README wrap guidance is
// generated from it). A real React component (window.React) so it composes with the React-mounted cards.
export function ThemeSurface(props) {
  // Content-sized (NOT 100vh): the storybook oracle captures each story at its own content height, so a
  // forced full-viewport height would add a dark void the reference never shows. Just the theme context —
  // dark ground, light ink, body font — so the components read the way the app hosts them.
  return React.createElement(
    'div',
    { style: { background: 'var(--koi-paper)', color: 'var(--koi-fg)', fontFamily: 'var(--koi-font-body)' } },
    props.children,
  );
}
`;

writeFileSync(resolve(distDir, '_ds_adapter_entry.mjs'), entry);
console.error(`gen-adapter-entry: wrapped ${list.length} components → dist/_ds_adapter_entry.mjs: ${list.join(', ')}`);
