        " * @template TQuery\n" +
        " * @template TResult\n" +
        " */\n" +
        "interface QueryHandler\n" +
        "{\n" +
        "    /** @param TQuery $query @return TResult */\n" +
        "    public function handle(mixed $query): mixed;\n" +
        "}\n";

